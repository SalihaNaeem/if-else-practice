// Examples of a simple if-else statement in JavaScript
// Example 1:
let grade = "A";

if (grade === "A") {
  console.log("Excellent!");
} else if (grade === "B") {
  console.log("Good job!");
} else if (grade === "C") {
  console.log("Well done!");
} else {
  console.log("Invalid grade");
}

// Example 2:
let age = 18;

if (age >= 18) {
  console.log("You are eligible to vote.");
} else if (age < 18) {
  console.log("You are not old enough to vote yet.");
}
// Example 3:
let mode = "dark";
let color;
if (mode === "dark") {
  color = "black";
} else if (mode === "light") {
  color = "white";
} else {
  color = "default";
}
console.log("Dark mode is enabled.");

// Example 4:
